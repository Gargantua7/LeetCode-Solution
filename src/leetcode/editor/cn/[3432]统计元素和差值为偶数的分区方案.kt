class Solution {
    fun countPartitions(nums: IntArray): Int {
        val sum = nums.sum()
        var res = 0
        var curr = 0

        for (i in 0..<nums.lastIndex) {
            curr += nums[i]
            if (abs(sum - curr * 2) % 2 == 0) res++
        }

        return res
    }
}
