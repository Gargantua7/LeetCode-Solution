class Solution {
    fun findTargetSumWays(nums: IntArray, target: Int): Int {
        var map = hashMapOf(0 to 1)
        nums.forEach { n ->
            val temp = HashMap<Int, Int>()
            map.forEach { (t, u) ->
                temp[t + n] = (temp[t + n]?: 0) + u
                temp[t - n] = (temp[t - n]?: 0) + u
            }
            map = temp
        }
        return map[target]?: 0
    }
