    class Solution {

        fun majorityElement(nums: IntArray): Int {
            val map = HashMap<Int, Int>()
            nums.forEach { num ->
                map[num] = 1 + (map[num] ?: 0)
                if (map[num]!! > nums.size / 2) return num
            }
            return nums[0]
        }
    }
