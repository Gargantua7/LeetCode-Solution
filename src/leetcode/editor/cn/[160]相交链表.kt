    /**
     * Example:
     * var li = ListNode(5)
     * var v = li.`val`
     * Definition for singly-linked list.
     * class ListNode(var `val`: Int) {
     *     var next: ListNode? = null
     * }
     */

    class Solution {

        fun getIntersectionNode(headA: ListNode?, headB: ListNode?): ListNode? {
            var count = 0
            var pA: ListNode? = headA ?: return null
            var pB: ListNode? = headB ?: return null
            while (pA != pB) {
                pA = if (pA == null) headB else pA.next
                pB = if (pB == null) headA else pB.next
            }
            return pA
        }
    }
//leetcode submit region end(Prohibit modification and deletion)

    fun getIntersectionNode1(headA: ListNode?, headB: ListNode?): ListNode? {
        var a = headA
        val stackA = Stack<ListNode>()
        while (a != null) {
            stackA.push(a)
            a = a.next
        }
        var b = headB
        val stackB = Stack<ListNode>()
        while (b != null) {
            stackB.push(b)
            b = b.next
        }
        var intersectVal: ListNode? = null
        while (stackA.isNotEmpty() && stackB.isNotEmpty()) {
            if (stackA.peek() == stackB.peek()) {
                intersectVal = stackA.pop()
                stackB.pop()
            } else break
        }
        return intersectVal
    }

