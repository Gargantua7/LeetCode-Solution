class CombinationSumIv {

    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {

        fun combinationSum4(nums: IntArray, target: Int): Int {
            val dp = IntArray(target + 1)
            dp[0] = 1
            for (i in 1..target) {
                for (j in nums) {
                    if (i >= j) dp[i] += dp[i - j]
                }
            }
            return dp[target]
        }
    }
