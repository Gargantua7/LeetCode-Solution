class Solution {
    fun countValidSelections(nums: IntArray): Int {
        val sum = nums.sum()
        var currSum = 0
        var ans = 0
        nums.forEach {
            if (it == 0) {
                val leftSum = currSum
                val rightSum = sum - currSum

                ans += when {
                    leftSum == rightSum -> 2
                    kotlin.math.abs(leftSum - rightSum) == 1 -> 1
                    else -> 0
                }
            }

            currSum += it
        }

        return ans
    }
}
