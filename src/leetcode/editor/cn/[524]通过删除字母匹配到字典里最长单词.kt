class Solution {
    fun findLongestWord(s: String, dictionary: List<String>): String {
        dictionary.sortedWith { a, b ->
            if (a.length != b.length) b.length - a.length
            else a.compareTo(b)
        }.forEach { str ->
            var index = 0
            s.forEach { c ->
                if (c == str[index] && index++ == str.lastIndex) return str
            }
        }
        return ""
    }
}
