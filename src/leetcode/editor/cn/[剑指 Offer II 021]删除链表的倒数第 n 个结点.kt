/**
 * Example:
 * var li = ListNode(5)
 * var v = li.`val`
 * Definition for singly-linked list.
 * class ListNode(var `val`: Int) {
 *     var next: ListNode? = null
 * }
 */
class Solution {
    fun removeNthFromEnd(head: ListNode?, n: Int): ListNode? {
        val new = ListNode(0)
        new.next = head
        val queue: Queue<ListNode> = LinkedList()
        var p: ListNode? = new
        while (p != null) {
            queue.offer(p)
            if (queue.size > n + 1) {
                queue.poll()
            }
            p = p.next
        }
        val node = queue.poll()
        node.next = node.next?.next
        return new.next
    }
}
