class Solution {
    fun divideArray(nums: IntArray, k: Int): Array<IntArray> {
        nums.sort()
        return Array(nums.size / 3) { row ->
            IntArray(3) { col -> nums[row * 3 + col] }.also {
                if (it.last() - it.first() > k) {
                    return emptyArray()
                }
            }
        }
    }
}
