class Solution {
    fun optimalDivision(nums: IntArray): String {
        return buildString {
            when (nums.size) {
                1 -> append(nums[0])
                2 -> append("${nums[0]}/${nums[1]}")
                else -> {
                    append("${nums[0]}/(${nums[1]}")
                    for (i in 2..nums.lastIndex)
                        append("/${nums[i]}")
                    append(")")
                }
            }
        }
    }
}
