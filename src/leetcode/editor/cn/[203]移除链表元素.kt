/**
 * Example:
 * var li = ListNode(5)
 * var v = li.`val`
 * Definition for singly-linked list.
 * class ListNode(var `val`: Int) {
 *     var next: ListNode? = null
 * }
 */
class Solution203 {
    fun removeElements(head: ListNode?, `val`: Int): ListNode? {
        var h = head
        var p = head
        var q: ListNode? = null
        while (p != null) {
            if (p.`val` == `val`) q?.let { it.next = p?.next } ?: let { h = p?.next } else q = p
            p = p.next
        }
        return h
    }
}
